let i = 0;

while (i < 10) {
    if (i % 2 == 0){
        console.log("Numero par", i);
        
    }
    i++;
}

console.log("End of While Loop");

//For loop has three parameters. The first is the inialization of a variable, then the condition, and then what will happen when the condition have been completed

for (let a = 2;a < 10;a++) {
    if (a % 2 == 0){
        console.log("Numero par", a);
    }
}

console.log("End of For Loop")